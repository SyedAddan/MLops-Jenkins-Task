class Insaan:
    def __init__(self):
        self.name = "Default"
        self.age = 0
    
    def setName(self, name):
        self.name = name
    
    def setAge(self, age):
        self.age = age
    
    def getName(self):
        return self.name
    
    def getAge(self):
        return self.age